using System;
namespace Barras
{
    public class Punto
    {
        double x;
        double y;

        public Punto()
        {
            x = y = 0;
        }

        public Punto(double _x, double _y)
        {
            x = _x;
            y = _y;
        }

        public void SetPunto(double ix, double iy)
        {
            x = ix;
            y = iy;
        }

        public double X
        {
            get { return x; }
            set { x = value; }
        }

        public double Y
        {
            get { return y; }
            set { y = value; }
        }
    }
}
